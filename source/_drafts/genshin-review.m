在地图设计上，璃月地区的整体设计水平明显优于蒙德地区。多山的地形使得地图的垂直设计有所体现，归离原上残破的古迹让探索感增强不少。作为蒙德地区到璃月地区的过渡地段，望舒客栈这一树形建筑起到了明显的吸引与引导作用，为玩家前往璃月港，感受不同国家风格做好了准备。

在进入璃月主城前，制作组特别设计了一段山间小道，并在出口开阔处设计了可以直接俯瞰璃月港的观景平台。无形的引导玩家引发对这个城市的兴趣。以桥作为入城的道路，加强了璃月港作为临海城市与水的关系。城内飞桥，跨街楼鳞次栉比，空间联系感明显提升。

虽然如此，但璃月地区仍存在一定的设计问题：

- 和蒙德地区的毛病一样，部分谜题过于简单，多次体验后就非常无聊；
- 关于<b><font color=#B69D36>岩元素</font></b>，并没有很好的通过地区设计体现出来，没有关于这个元素设计明显的机制；
- 场景空旷感的问题依旧存在，但相比蒙德已经有所进步；
- 可能是设计时的技术力原因，内河与外海并没有沟通到一起，无法通过浪船等工具来进行场景互动，有些遗憾；

<br>

<br>

### 龙脊雪山：漩涡与落星

1.2版本更新的全新地区是龙脊雪山，附属于蒙德地区。

为了实现从草地过渡到冰原的效果，制作组采用的是以一条河作为分界线，两岸场景变化的过渡方式。进入雪山后，角色会被赋予一个寒冷值的debuff，随时间增长，并在积累一定寒冷值后开始损失生命值。这个机制成为了探索雪山最大的动力，但同时也成了探索雪山时的一大桎梏。坦诚地说，这是《原神》在探索中加入环境特殊机制的尝试，但是实现的效果不尽人意。

这个机制过分限制玩家的活动范围，在没有找到火堆等减少寒冷值的设施下，这套机制只能让我不断地吃药或者是返回传送点，挫败感较强。制作组有意地在探索的道路上设置了遗迹之烛和火精灵等取暖设施，但是只是在制作组设计好的任务必经道路上放置，我在进行其他地区的探索时没感受到这套取暖机制发挥了什么作用。

雪山的探索必须通过一个世界任务来开启，意味着在任务进度没有推进前，我是不太能够自己进入雪山探索的，只能在边缘徘徊。开启系列世界任务后，玩家可以自由选择三个支线任务的先后探索顺序，然而这三个支线任务存在的意义就是让我不断重复 挖矿→ 打旁边的怪 → 带着矿打冰块 的流程，我没有体验到开放世界开放在了哪里。

但雪山的地图设计也有一定的进步，星荧洞窟就是一个很好的例子。制作组在洞窟内部的设计水平有所提升，地图层次感开始显现，我可以感觉到整个洞窟是存在多个出口，四通八达的。但是制作组的设计较为收敛，地图空间的划分仅以几条冰连廊构成，内容还是主要为了底层的任务而设计，我觉得上层的设计可以更加丰富一些。

<br>

<br>

### 稻妻：寂远无妄之国

2.0大版本的更新是《原神》在开服一年后地图设计水平进步的一大证明。稻妻作为第三个开放的国家，以日本幕府作为原型，是<b><font color=#B187F0>雷元素</font></b>的代表国度。

作为岛国，来往于稻妻诸岛的交通方式主要依靠浪船这一交通工具，在海上航行成为了稻妻大世界体验的一个组成部分。这一设计无疑是非常具有探索驱动力的，驱船探险的体验让我记忆深刻。

稻妻的地图设计存在天然优势，可以设计多个不同景观，不同地形，不同主题的岛屿，由剧情进行串联。《原神》在这方面做的不错，每个岛屿都能做出特点和记忆点。

- 作为任务起始点，铺垫稻妻第一印象的离岛，遍地红枫，渔港繁华；
- 遍布夜樱和白狐的鸣神岛，作为稻妻主城所在，静寂幽玄；
- 疯狂打雷下雨的阴间踏鞲砂，残骸遍地，色调凄凉；
- 影射核辐射事故的八酝岛，独特的“雷场”机制与雪山寒冷值相似但做得更好，索道的连接扩展了空间的联系度；
- 以雷暴风团醒目，从稻妻各处都能看见的清籁岛，场景氛围极好，雷鸣阵阵，系列任务也做的不错，将雷极移动的机制发挥得很好；
- 环境优美的海祇岛，让经历过前面一连串阴间岛屿的玩家心旷神怡，场景中的水母、泡泡增添了环境与玩家的粘合度，四周流水汇集至珊瑚宫中央深池的美景同时暗示着地底地图的存在；
- 雾海弥漫的鹤观岛，大雾是其特殊的环境机制。环境氛围感还是挺不错的，但是漫天大雾下游戏机制没能做到很好的配合，导致任务设计配不上这张地图。同时，系列任务完成后大雾便会散去，使岛屿失去特殊的地方，令人遗憾。

<br>

稻妻的地图给很多玩家（包括我）的最深的印象就是“到处都在地下”。制作组不满足于蒙德、璃月的地表大地图，进而开始追求地表地底的内容充实化。但是制作组的部分设计较为激进，甚至有点走偏的感觉。

部分地下洞窟纯粹为了任务而设计，导致没做任务的我进去后一脸茫然找不到出路。部分场景设计的太过于狭窄，配合雷元素的紫色色调，场景的压迫感导致我探索的时候压力有点大。制作组认识到了蒙德、璃月地图存在路过就再也不会重来的一次性探图问题，在稻妻的地下洞窟探索中加入了需要收集一定雷之印才能进入的地方，然而之前探索的糟糕体验已经让我不想再一次进入这个洞窟继续探索。

稻妻的地图设计让我容易感到烦躁，但不得不承认的是《原神》在稻妻这个国家的地图设计上投入了很大心血，进步也是可见的。

<br>

<br>

### 渊下宫：白夜之光

2.4版本（2022年1月左右）更新开放的渊下宫，是《原神》地图设计上的一次进化。直到现在的3.4版本，我最喜欢的一张地图仍然是渊下宫。

